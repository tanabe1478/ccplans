function randomItem<T>(arr: T[]): T {
  return arr[Math.floor(Math.random() * arr.length)];
}

/**
 * Generate a random plan filename following the pattern:
 * [adjective]-[adjective]-[noun].md
 */
export function generatePlanName(): string {
  const adj1 = randomItem(adjectives);
  let adj2 = randomItem(adjectives);

  // Ensure two different adjectives
  while (adj2 === adj1) {
    adj2 = randomItem(adjectives);
  }

  const noun = randomItem(nouns);
  return `${adj1}-${adj2}-${noun}.md`;
}
